// Switch statement
// control flow that can replace multiple if statements
// break breaks or will continue the process
// default is activated if another value is used
const day = 2;
switch(day) {
  case 1:
    console.log("Sunday");
    break;
    case 2:
    console.log("Monday");
    break;
    case 3:
    console.log("Tuesday");
    break;
    case 4:
    console.log("Wednesday");
    break;
    case 5:
    console.log("Thursday");
    break;
    case 6:
    console.log("Friday");
    break;
    default:
      console.log("I don't know that!")
}
